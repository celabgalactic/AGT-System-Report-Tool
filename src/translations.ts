export const translateColumnHeader = (headerName: string, lang: LanguageCode): string => {
  const normalized = headerName.trim();
  const langTranslations = COLUMN_TRANSLATIONS[lang];
  if (langTranslations && langTranslations[normalized]) {
    return langTranslations[normalized];
  }
  if (langTranslations) {
    const key = Object.keys(langTranslations).find(k => k.toLowerCase() === normalized.toLowerCase());
    if (key) {
      return langTranslations[key];
    }
  }
  return headerName;
};
